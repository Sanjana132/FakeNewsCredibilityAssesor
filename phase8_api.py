@app.get("/health")
async def health():
    return {
        "status": "ready" if store.ready else "loading",
        "models": {
            "tfidf":   store.tfidf_bundle is not None,
            "deberta": store.deberta_model is not None,
        },
        "speaker_profiles": len(store.speaker_profiles),
        "dataset_stats":    store.dataset_stats,
    }


@app.post("/predict", response_model=PredictResponse)
async def predict(req: PredictRequest):
    if not store.ready:
        raise HTTPException(503, "Server is still loading models.")

    text    = req.text.strip()
    speaker = req.speaker.strip()
    context = req.context.strip()

    # Resolve context prior
    prior = req.context_prior
    speaker_info = store.speaker_profiles.get(speaker.lower())
    if prior is None:
        if speaker_info:
            prior = speaker_info.get("bayes_score", 0.5)
        else:
            prior = 0.5

    features = _compute_features(text, context, prior)

    tfidf_s   = predict_tfidf(text, features)
    deberta_r = predict_deberta(text, speaker, context, prior,
                                features, n_passes=req.mc_passes)
    result    = ensemble_score(tfidf_s, deberta_r)

    return PredictResponse(
        **result,
        speaker_profile=speaker_info,
        context_prior_used=round(prior, 4),
    )


@app.get("/speaker/{speaker_name}")
async def get_speaker(speaker_name: str):
    key = speaker_name.lower()
    profile = store.speaker_profiles.get(key)
    if profile is None:
        raise HTTPException(
            404,
            f"No profile for '{speaker_name}'. "
            "Run Phase 6 to build speaker profiles.")
    return profile


@app.get("/speakers/top")
async def top_speakers(n: int = 20, min_claims: int = 3,
                       order: str = "asc"):
    profiles = [p for p in store.speaker_profiles.values()
                if p["n_claims"] >= min_claims]
    reverse  = (order == "desc")
    ranked   = sorted(profiles, key=lambda p: p["bayes_score"],
                      reverse=reverse)
    return {"order": order, "speakers": ranked[:n]}


@app.get("/speakers/search")
async def search_speakers(q: str):
    q_low = q.lower()
    matches = [p for k, p in store.speaker_profiles.items()
               if q_low in k]
    return {"query": q, "matches": matches[:20]}


