FEAT_COLS = [
    "vader_compound", "vader_pos", "vader_neg", "vader_neu",
    "pos_word_count", "neg_word_count", "pos_neg_ratio", "sentiment_extremity",
    "context_sentiment_risk", "context_adjusted_sentiment",
    "persuasive_context_flag", "context_credibility_prior", "token_length_approx",
]

def _compute_features(text: str, context: str,
                      context_prior: float = 0.5) -> list[float]:
    """
    Compute the 13 engineered features for a single claim at inference time.

    Delegates to the canonical Phase 2 extract_all_features() so the numbers
    match training EXACTLY. A previous local reimplementation drifted — it used
    fractional opinion-word counts, an unbounded pos/neg ratio, and abs(compound)
    for extremity — none of which matched the values the TF-IDF scaler and the
    DeBERTa fusion head were fitted on, quietly degrading every prediction.
    """
    try:
        from credibility_detector_phases123 import (
            extract_all_features, normalise_context)
        ctx   = normalise_context(context)
        feats = extract_all_features(text, ctx)          # 11 sentiment/interaction feats
        feats["context_credibility_prior"] = float(context_prior)
        feats["token_length_approx"]       = len(str(text)) / 4.0
        return [float(feats.get(c, 0.0)) for c in FEAT_COLS]
    except Exception:
        # Keep the vector shape correct even if imports/NLTK data are missing.
        return [0.0] * len(FEAT_COLS)


def predict_tfidf(text: str, features: list[float]) -> Optional[float]:
    if store.tfidf_bundle is None:
        return None
    from scipy.sparse import hstack, csr_matrix
    b = store.tfidf_bundle
    X_text = b["vectorizer"].transform([text])
    feat_arr = np.array(features).reshape(1, -1)
    X_feat = csr_matrix(b["scaler"].transform(feat_arr))
    X = hstack([X_text, X_feat])
    score = float(np.clip(b["model"].predict(X)[0], 0.0, 1.0))
    return score


def predict_deberta(text: str, speaker: str, context: str,
                    prior: float, features: list[float],
                    n_passes: int = 20) -> Optional[dict]:
    if store.deberta_model is None or store.p5 is None:
        return None
    result = store.p5.predict_with_uncertainty(
        model=store.deberta_model,
        tokenizer=store.deberta_tok,
        text=text,
        speaker=speaker,
        context=context,
        prior=prior,
        features=features,
        n_passes=n_passes,
        device=store.deberta_device,
    )
    return result


def ensemble_score(tfidf: Optional[float],
                   deberta: Optional[dict]) -> dict:
    """
    Weighted ensemble: 0.3 × TF-IDF + 0.7 × DeBERTa.
    Falls back to whichever model is available.
    """
    if deberta is not None and tfidf is not None:
        score = 0.3 * tfidf + 0.7 * deberta["mean"]
        lower = 0.3 * tfidf + 0.7 * deberta["lower"]
        upper = 0.3 * tfidf + 0.7 * deberta["upper"]
        model_used = "ensemble (DeBERTa×0.7 + TF-IDF×0.3)"
    elif deberta is not None:
        score = deberta["mean"]
        lower = deberta["lower"]
        upper = deberta["upper"]
        model_used = "deberta"
    elif tfidf is not None:
        score = tfidf
        lower = max(0.0, tfidf - 0.12)
        upper = min(1.0, tfidf + 0.12)
        model_used = "tfidf_baseline"
    else:
        raise HTTPException(503, "No models loaded. Run Phase 4 and/or Phase 5 first.")

    score = float(np.clip(score, 0.0, 1.0))
    verdict = ("FALSE" if score < 0.35 else
               "MIXED / UNVERIFIED" if score < 0.65 else "TRUE")
    return {
        "score":        round(score, 4),
        "lower_90ci":   round(float(np.clip(lower, 0.0, 1.0)), 4),
        "upper_90ci":   round(float(np.clip(upper, 0.0, 1.0)), 4),
        "verdict":      verdict,
        "model_used":   model_used,
    }


