# ── Request / Response schemas ────────────────────────────────────────────────

class PredictRequest(BaseModel):
    text:    str   = Field(...,  example="Obama tripled the national debt.",
                           description="The claim or statement to evaluate.")
    speaker: str   = Field("",  example="Donald Trump",
                           description="Who made the claim (optional).")
    context: str   = Field("",  example="a campaign rally",
                           description="Where the claim was made (optional).")
    context_prior: Optional[float] = Field(
        None, ge=0.0, le=1.0,
        description="Override data-driven context prior [0–1]. "
                    "Auto-looked up from speaker profile if omitted.")
    mc_passes: int = Field(20, ge=1, le=100,
                           description="MC Dropout forward passes for CI.")


class PredictResponse(BaseModel):
    score:       float
    lower_90ci:  float
    upper_90ci:  float
    verdict:     str
    model_used:  str
    speaker_profile: Optional[dict] = None
    context_prior_used: float


