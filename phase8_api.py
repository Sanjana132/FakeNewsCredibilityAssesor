def _load_phase5_module():
    spec = importlib.util.spec_from_file_location(
        "phase5", _HERE / "phase5_deberta.py")
    p5 = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(p5)
    return p5


class ModelStore:
    """Holds all loaded models. Populated at startup, reused per request."""

    def __init__(self):
        self.tfidf_bundle   = None   # {vectorizer, scaler, model, feature_cols}
        self.deberta_model  = None
        self.deberta_tok    = None
        self.deberta_device = None
        self.p5             = None
        self.speaker_profiles: dict = {}
        self.dataset_stats:  dict = {}
        self.ready          = False

    def load(self):
        print("  Loading models…")
        self._load_tfidf()
        self._load_deberta()
        self._load_speaker_profiles()
        self._load_stats()
        self.ready = True
        print("  ✓ All models loaded.")

    def _load_tfidf(self):
        p = MODEL_DIR / "baseline_tfidf.pkl"
        if not p.exists():
            print(f"  WARNING: {p} not found — TF-IDF model unavailable.")
            return
        self.tfidf_bundle = joblib.load(p)
        print(f"  TF-IDF baseline loaded.")

    def _load_deberta(self):
        tok_path = MODEL_DIR / "deberta_tokenizer"
        wt_path  = MODEL_DIR / "deberta_best.pt"
        if not tok_path.exists() or not wt_path.exists():
            print("  WARNING: DeBERTa weights not found — using TF-IDF only.")
            return
        try:
            self.p5 = _load_phase5_module()
            device  = self.p5.detect_device()
            tok = AutoTokenizer.from_pretrained(str(tok_path), use_fast=False)
            model = self.p5.DeBERTaCredibilityModel().to(device)
            model.load_state_dict(
                torch.load(str(wt_path), map_location=device))
            model.eval()
            self.deberta_model  = model
            self.deberta_tok    = tok
            self.deberta_device = device
            print(f"  DeBERTa loaded on {device}.")
        except Exception as e:
            print(f"  WARNING: DeBERTa load failed ({e}) — using TF-IDF only.")

    def _load_speaker_profiles(self):
        p = MODEL_DIR / "speaker_profiles.json"
        if not p.exists():
            return
        data = json.loads(p.read_text())
        self.speaker_profiles = {
            r["speaker"].lower(): r for r in data.get("profiles", [])
        }
        print(f"  Speaker profiles: {len(self.speaker_profiles):,} loaded.")

    def _load_stats(self):
        p = DATA_DIR / "dataset_stats.json"
        if p.exists():
            self.dataset_stats = json.loads(p.read_text())


